# Initialize variables of different types
integer_var = 42
float_var = 3.14
string_var = "Hello, World!"
boolean_var = True

# Create a list of five Nigerian states
nigerian_states = ["Lagos", "Kano", "Rivers", "Oyo", "Abuja"]

# Create a tuple with three currency codes
currency_codes = ("NGN", "USD", "EUR")

# Create a set of unique student IDs
student_ids = {101, 102, 103, 104, 105}

# Create a dictionary mapping student names to scores
student_scores = {
    "Alice": 85,
    "Bob": 90,
    "Charlie": 78,
    "David": 88,
    "Eva": 92
}

# Print the variables to verify their contents
print("Integer:", integer_var)
print("Float:", float_var)
print("String:", string_var)
print("Boolean:", boolean_var)
print("Nigerian States:", nigerian_states)
